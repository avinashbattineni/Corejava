package javapractice;

public class Switchstatement
{

    public static void main(String[] args)
    {
        int i = 10;
        switch (i)
        {
            case 10:
            {
                System.out.println("first case");
                break;
            }
            case 2:
            {
                System.out.println("second case");
                break;
            }
            case 3:
            {
                System.out.println("tenth case");
                break;

            }

            default:
            {

                System.out.println("hello");
                break;
            }
        }
    }
}

