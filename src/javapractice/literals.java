package javapractice;

public class literals
{
    public static void main(String[] args) {
        byte b = 10;
        short s = 20;
        int i = 30;
        long l = 40;
        char ch = 'a';
        float f = 12.5f;
        double d = 124.555;
        boolean bo = true;
        String st = "abc";
        String str = "123";
        String str1 = "true";
        String str2 = "12.5f";
        String str3 = "125.5555";
        System.out.println(b+" "+ s+ " "+ i+ " "+ l+ " "+ ch+ " "+ f+ " "+ d+ " "+ bo+ " "+ st+ " "+ str+ " "+ str1+ " "+ str2+ " "+ str3);
    }
}
