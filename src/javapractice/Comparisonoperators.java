package javapractice;

public class Comparisonoperators
{
    public static void main(String[]args)
   {
       //comparison(==,!=,<,<=,>,>=)
       int a=10;
       int b=20;
       System.out.println("a: "+a);
       System.out.println("b: "+b);
       System.out.println("a==b: "+(a==b));
       System.out.println("a!=b:"+ (a!=b));
       System.out.println("a<b: "+ (a<b));
       System.out.println("a<=b: "+ (a<=b));
       System.out.println("a>b: "+ (a>b));
       System.out.println("a>=b: "+(a>=b));
       System.out.println();

   }
}
